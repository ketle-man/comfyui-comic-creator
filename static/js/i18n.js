const _I18N_LS_KEY = 'ccc_ui_lang';
const _I18N_DEFAULT_LANG = 'ja';
let _cccLang = localStorage.getItem(_I18N_LS_KEY) || _I18N_DEFAULT_LANG;
if (!LANGUAGES[_cccLang]) _cccLang = _I18N_DEFAULT_LANG;

function t(key, ...args) {
    const dict = LANGUAGES[_cccLang] || LANGUAGES[_I18N_DEFAULT_LANG];
    const val = dict[key] ?? LANGUAGES[_I18N_DEFAULT_LANG][key] ?? key;
    return typeof val === 'function' ? val(...args) : val;
}

// py/ccc.py が返す error_code/error_params を t('err.xxxCamelCase') にマッピングして多言語文言を返す。
// error_code が無い（＝サーバー側で個別対応していない例外）場合は null を返すので、
// 呼び出し側は既存の t('...') フォールバック文言と組み合わせて使うこと
// （例: resolveBackendError(data.error_code, data.error_params) || t('common.someFallback')）。
function resolveBackendError(errorCode, errorParams) {
    if (!errorCode) return null;
    const key = 'err.' + errorCode.replace(/_([a-z])/g, (_, c) => c.toUpperCase());
    const params = errorParams ? Object.values(errorParams) : [];
    return t(key, ...params);
}

function getLang() {
    return _cccLang;
}

function setLang(lang) {
    if (!LANGUAGES[lang]) return;
    _cccLang = lang;
    localStorage.setItem(_I18N_LS_KEY, lang);
}

function getLanguageOptions() {
    return { ja: '日本語', en: 'English', zh: '中文' };
}

// root配下の [data-i18n] / [data-i18n-placeholder] / [data-i18n-title] / [data-i18n-alt] 属性を
// 現在の言語で反映する。言語切替直後の再適用にも同じ関数を使える。
function applyI18nToHtml(root) {
    const scope = root || document;
    scope.querySelectorAll('[data-i18n]').forEach(el => {
        el.textContent = t(el.dataset.i18n);
    });
    scope.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.setAttribute('placeholder', t(el.dataset.i18nPlaceholder));
    });
    scope.querySelectorAll('[data-i18n-title]').forEach(el => {
        el.setAttribute('title', t(el.dataset.i18nTitle));
    });
    scope.querySelectorAll('[data-i18n-alt]').forEach(el => {
        el.setAttribute('alt', t(el.dataset.i18nAlt));
    });
}
